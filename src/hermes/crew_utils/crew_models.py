class PeerReviewFeedback(BaseModel):
    strengths: List[str] = Field(..., description="Strengths of the research")
    weaknesses: List[str] = Field(..., description="Weaknesses or areas for improvement")
    suggestions: List[str] = Field(..., description="Suggestions for addressing weaknesses")
    overall_assessment: str = Field(..., description="Overall assessment of the research")
    
